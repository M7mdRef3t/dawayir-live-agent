import { BUCKET_NAME } from '../config/index.js';
import {
    getUserArtifacts,
    readCognitiveArtifacts,
    writeCognitiveArtifacts,
} from '../services/cognitive-artifacts.js';

const logInfo = (...args) => console.log('[dawayir-server]', ...args);
const logError = (...args) => console.error('[dawayir-server:error]', ...args);

// ── get_expert_insight ───────────────────────────────────
export const handleGetExpertInsight = (call, args, knowledgeBase) => {
    const topic = (args.topic || '').toLowerCase().trim();
    logInfo(`[Grounding] get_expert_insight called for topic: "${topic}"`);

    if (!knowledgeBase) {
        throw new Error('Knowledge base not loaded');
    }

    const principle = knowledgeBase.principles.find((p) =>
        p.id === topic
        || p.concept.toLowerCase().includes(topic)
        || p.description.toLowerCase().includes(topic)
    );

    if (principle) {
        logInfo(`[Grounding] Found principle: ${principle.concept}`);
        return {
            id: call.id,
            name: call.name,
            response: {
                result: {
                    ok: true,
                    platform: knowledgeBase.platform_name,
                    concept: principle.concept,
                    description: principle.description,
                    guideline: principle.guideline,
                    persona_rules: knowledgeBase.agent_persona_rules,
                },
            },
        };
    }

    logInfo(`[Grounding] No exact match for "${topic}", returning full framework`);
    return {
        id: call.id,
        name: call.name,
        response: {
            result: {
                ok: true,
                platform: knowledgeBase.platform_name,
                philosophy: knowledgeBase.core_philosophy,
                all_principles: knowledgeBase.principles.map((p) => ({
                    concept: p.concept,
                    description: p.description,
                    guideline: p.guideline,
                })),
                tone_system: knowledgeBase.tone_system,
                orbital_dictionary: knowledgeBase.orbital_dictionary,
                success_stories: knowledgeBase.success_stories,
                emergency_protocol: knowledgeBase.emergency_protocol,
                dawayir_model: knowledgeBase.dawayir_circle_model,
                persona_rules: knowledgeBase.agent_persona_rules,
            },
        },
    };
};

// ── save_mental_map ──────────────────────────────────────
export const handleSaveMentalMap = (call, args) => {
    const sessionName = args.session_name || `session_${Date.now()}`;
    logInfo(`[Memory] save_mental_map called: ${sessionName}`);
    return {
        id: call.id,
        name: call.name,
        response: { result: { ok: true, saved_as: sessionName } },
    };
};

// ── generate_session_report ──────────────────────────────
export const handleGenerateSessionReport = (call, args, metrics = {}, storage = null) => {
    const { summary, insights, recommendations } = args;
    logInfo('[Memory] generate_session_report called');

    const evidence = `
### Cognitive OS Performance Metrics (Evidence)
- **Final Equilibrium Score:** ${((metrics.equilibriumScore || 0) * 100).toFixed(1)}%
- **Session Clarity Delta:** ${(((metrics.clarityDelta) || 0) * 100).toFixed(1)}%
- **Peak Overload Index:** ${((metrics.overloadIndex || 0) * 100).toFixed(1)}%
- **Total Operational Cycles:** ${metrics.interactionCount || 0}
`;

    if (BUCKET_NAME && storage) {
        const filename = `session_report_${Date.now()}.md`;
        const reportContent = `# Dawayir Session Report
**Date:** ${new Date().toLocaleString()}

## Executive Summary
${summary || 'N/A'}

## Core Insights
${insights || 'N/A'}

${evidence}

## Recommendations
${recommendations || 'N/A'}

---
*Generated by Dawayir Cognitive Kernel (Gemini 2.0 Flash)*`;

        const file = storage.bucket(BUCKET_NAME).file(filename);
        file.save(reportContent, { contentType: 'text/markdown' })
            .then(() => logInfo(`GCS Report Upload Successful: ${filename}`))
            .catch((err) => logError('GCS Report Upload Failed:', err));
    }

    return {
        id: call.id,
        name: call.name,
        response: {
            result: {
                ok: true,
                summary,
                insights,
                recommendations,
                metrics,
                timestamp: new Date().toISOString(),
            },
        },
    };
};

// ── create_truth_contract ────────────────────────────────
export const handleCreateTruthContract = (call, args, artifactsPath, userKey = 'anonymous') => {
    const action = String(args?.action || '').trim();
    const title = String(args?.title || 'Truth Contract').trim();
    const anchor = String(args?.anchor || '').trim();
    const dueHoursRaw = Number(args?.due_hours);
    const dueHours = Number.isFinite(dueHoursRaw) && dueHoursRaw > 0 ? dueHoursRaw : 24;

    if (!action) {
        throw new Error('create_truth_contract requires action');
    }

    const now = Date.now();
    let truthContract = {
        title: title || 'Truth Contract',
        action,
        anchor,
        createdAt: new Date(now).toISOString(),
        dueAt: new Date(now + (dueHours * 60 * 60 * 1000)).toISOString(),
    };

    try {
        const current = readCognitiveArtifacts(artifactsPath);
        const next = { ...current, users: { ...(current.users || {}) } };
        const userData = { ...getUserArtifacts(next, userKey) };
        const existingContracts = Array.isArray(userData.truthContracts) ? [...userData.truthContracts] : [];
        const dedupeWindowMs = 24 * 60 * 60 * 1000;
        const existingIndex = existingContracts.findIndex((entry) => (
            String(entry?.action || '').trim() === action
            && Math.abs(now - new Date(entry?.createdAt || 0).getTime()) <= dedupeWindowMs
        ));

        if (existingIndex >= 0) {
            const existing = existingContracts[existingIndex];
            truthContract = {
                ...existing,
                title: title || existing?.title || 'Truth Contract',
                anchor: anchor || existing?.anchor || '',
                dueAt: new Date(now + (dueHours * 60 * 60 * 1000)).toISOString(),
            };
            existingContracts[existingIndex] = truthContract;
        } else {
            existingContracts.push(truthContract);
        }

        userData.truthContracts = existingContracts;
        next.users[userKey] = userData;
        writeCognitiveArtifacts(artifactsPath, next);
    } catch (error) {
        logError('[Memory] Failed to persist truth contract:', error);
    }

    return {
        id: call.id,
        name: call.name,
        response: { result: { ok: true, truthContract } },
    };
};

// ── Main dispatcher ──────────────────────────────────────
export const resolveServerToolCalls = (functionCalls, liveSession, metrics = {}, context = {}) => {
    if (!liveSession || functionCalls.length === 0) return;

    const responses = [];

    for (const call of functionCalls) {
        let args = call?.args ?? {};
        if (typeof args === 'string') {
            try { args = JSON.parse(args); } catch { args = {}; }
        }

        try {
            switch (call.name) {
                case 'get_expert_insight':
                    responses.push(handleGetExpertInsight(call, args, context.knowledgeBase));
                    break;
                case 'save_mental_map':
                    responses.push(handleSaveMentalMap(call, args));
                    break;
                case 'generate_session_report':
                    responses.push(handleGenerateSessionReport(call, args, metrics, context.storage));
                    break;
                case 'create_truth_contract': {
                    const response = handleCreateTruthContract(
                        call,
                        args,
                        context.artifactsPath,
                        context.userKey || 'anonymous',
                    );
                    responses.push(response);
                    const truthContract = response?.response?.result?.truthContract || null;
                    if (truthContract && typeof context.onTruthContract === 'function') {
                        context.onTruthContract(truthContract);
                    }
                    break;
                }
                default:
                    logInfo(`[Tool] Unhandled server-side tool: ${call.name}`);
                    break;
            }
        } catch (error) {
            logError(`[Tool] Server-side tool error (${call.name}):`, error);
            responses.push({
                id: call.id,
                name: call.name,
                response: { result: { ok: false, error: error.message } },
            });
        }
    }

    if (responses.length > 0) {
        logInfo(`[Tool] Sending ${responses.length} server-resolved tool response(s) to Gemini`);
        liveSession.sendToolResponse({ functionResponses: responses });
    }
};
